// Math.abs()  - absolute value (laging positive)
console.log("Math.abs(-10) =", Math.abs(-10));   // 10

// Math.round() - i-round sa pinakamalapit na buo
console.log("Math.round(4.6) =", Math.round(4.6)); // 5

// Math.floor() - i-round pababa
console.log("Math.floor(4.9) =", Math.floor(4.9)); // 4

// Math.ceil()  - i-round pataas
console.log("Math.ceil(4.1) =", Math.ceil(4.1));   // 5

// Math.random() - random number 0 hanggang mas mababa sa 1
console.log("Math.random() =", Math.random());

// Math.pow()   - exponent (base ^ exponent)
console.log("Math.pow(2, 3) =", Math.pow(2, 3));   // 8

// Math.sqrt()  - square root
console.log("Math.sqrt(16) =", Math.sqrt(16));     // 4

// Math.max() at Math.min() - pinakamataas at pinakamababa
let numbers = [10, 50, 20];
console.log("Math.max(...numbers) =", Math.max(...numbers)); // 50
console.log("Math.min(...numbers) =", Math.min(...numbers)); // 10
